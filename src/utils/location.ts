export async function fetchCities(countryCode: string, stateCode: string): Promise<string[]> {
    // Primeiro, verificar se temos cidades na lista local
    const normalizedCountryCode = countryCode === 'USA' ? 'US' : countryCode;
    if (COUNTRY_CITIES_MAP[normalizedCountryCode] && COUNTRY_CITIES_MAP[normalizedCountryCode][stateCode]) {
        return [...COUNTRY_CITIES_MAP[normalizedCountryCode][stateCode]].sort();
    }

    // Para Brasil, usar API do IBGE
    if (normalizedCountryCode === 'BR') {
        try {
            const response = await fetch(`https://servicodados.ibge.gov.br/api/v1/localidades/estados/${stateCode}/municipios`);
            const data = await response.json();
            return data.map((city: any) => city.nome).sort();
        } catch (error) {
            console.error('Error fetching IBGE cities:', error);
            return [];
        }
    }

    // Para outros países, tentar API externa
    try {
        const countryName = normalizedCountryCode === 'US' ? 'United States' : 
                           normalizedCountryCode === 'GB' ? 'United Kingdom' :
                           COUNTRIES.find(c => c.code === normalizedCountryCode)?.name || normalizedCountryCode;
        
        // Buscar o nome do estado a partir do código
        const states = getStatesForCountry(countryCode);
        const state = states.find(s => s.code === stateCode);
        const stateName = state?.name || stateCode;
        
        const response = await fetch('https://countriesnow.space/api/v0.1/countries/state/cities', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ country: countryName, state: stateName })
        });
        const result = await response.json();
        if (!result.error && result.data) {
            return result.data.sort();
        }
    } catch (error) {
        console.error('Error fetching global cities:', error);
    }

    return [];
}

// Função auxiliar para obter estados de um país
